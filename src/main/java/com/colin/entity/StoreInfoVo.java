package com.colin.entity;

public class StoreInfoVo {
    private Integer storeNo;
    private String storeName;
    private String storeManagerCh;
    private String storeManagerEn;
    private String storeTel;
    private String storeManagerTel;
    private String storeEmail;

    public Integer getStoreNo() {
        return storeNo;
    }

    public void setStoreNo(Integer storeNo) {
        this.storeNo = storeNo;
    }

    public String getStoreName() {
        return storeName;
    }

    public void setStoreName(String storeName) {
        this.storeName = storeName;
    }

    public String getStoreManagerCh() {
        return storeManagerCh;
    }

    public void setStoreManagerCh(String storeManagerCh) {
        this.storeManagerCh = storeManagerCh;
    }

    public String getStoreManagerEn() {
        return storeManagerEn;
    }

    public void setStoreManagerEn(String storeManagerEn) {
        this.storeManagerEn = storeManagerEn;
    }

    public String getStoreTel() {
        return storeTel;
    }

    public void setStoreTel(String storeTel) {
        this.storeTel = storeTel;
    }

    public String getStoreManagerTel() {
        return storeManagerTel;
    }

    public void setStoreManagerTel(String storeManagerTel) {
        this.storeManagerTel = storeManagerTel;
    }

    public String getStoreEmail() {
        return storeEmail;
    }

    public void setStoreEmail(String storeEmail) {
        this.storeEmail = storeEmail;
    }
}
